# This program will calculate the profit made from sales. 

# Define the percentage percentage of profit
profitPercent = 0.23

# Have the user input the project amount of total sales
projectedSales = float(input('Enter the total amount of projected sales: '))

# Calculate total profit from sales.
totalProfit = projectedSales * profitPercent

# Output the total profit earned.
print(f'The calculated total profit is {totalProfit}')
